    void Browser::OnDraw(Ref<UI::Composer> Composer)
    {
        Composer.SetNextWindowSize(900, 500, ImGuiCond_FirstUseEver);

        if (Composer.Begin(GetTitle(), mVisible))
        {
            DrawToolbar(Composer);
            Composer.Separator();

            Composer.BeginChild("##sidebar", ImVec2(180, 0), ImGuiChildFlags_Borders);
            DrawSidebar(Composer);
            Composer.EndChild();

            Composer.SameLine();

            Composer.BeginChild("##grid");
            DrawGrid(Composer);
            Composer.EndChild();
        }
        Composer.End();
    }

